export function getNutrition(item: string): NutritionInfo {
  return NUTRITION[item] ?? { grams: 200, kcal: 280 }
}

export function getMealNutrition(items: string[]): { totalGrams: number; totalKcal: number } {
  return items.reduce(
    (acc, item) => {
      const n = getNutrition(item)
      return { totalGrams: acc.totalGrams + n.grams, totalKcal: acc.totalKcal + n.kcal }
    },
    { totalGrams: 0, totalKcal: 0 }
  )
}
